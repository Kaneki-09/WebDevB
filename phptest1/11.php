<?php
// 下記のフォームから正しい郵便番号形式で送られてくるかを正規表現で判定するプログラムzip . phpを記述してください。

if ($_POST) {
    $zip1 = $_POST['zip1'];
    $zip2 = $_POST['zip2'];


    // 郵便番号の形式をチェック
    if (preg_match('/^\d{3}-\d{4}$/', "$zip1-$zip2")) {
        echo "正しい郵便番号形式です: $zip1-$zip2";
    } else {
        echo "郵便番号の形式が正しくありません。例: 123-4567";
    }
}
